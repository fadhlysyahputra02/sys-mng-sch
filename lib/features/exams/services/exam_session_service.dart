// ─────────────────────────────────────────────────────────────
//  ExamSessionService — CRUD untuk exam_events & exam_sessions
// ─────────────────────────────────────────────────────────────
class ExamSessionService {
  final FirebaseFirestore _db = FirebaseFirestore.instance;

  // ── References ──────────────────────────────────────────────
  CollectionReference<Map<String, dynamic>> _eventsRef(String schoolId) =>
      _db.collection('schools').doc(schoolId).collection('exam_events');

  CollectionReference<Map<String, dynamic>> _sessionsRef(String schoolId) =>
      _db.collection('schools').doc(schoolId).collection('exam_sessions');

  CollectionReference<Map<String, dynamic>> _participationsRef(
          String schoolId, String sessionId) =>
      _sessionsRef(schoolId).doc(sessionId).collection('participations');

  // ── ExamEvent CRUD ───────────────────────────────────────────

  /// Membuat ExamEvent baru, mengembalikan ID dokumen yang dibuat
  Future<String> createExamEvent({
    required String schoolId,
    required ExamEvent event,
  }) async {
    final docRef = _eventsRef(schoolId).doc();
    final newEvent = ExamEvent(
      id: docRef.id,
      title: event.title,
      examType: event.examType,
      startDate: event.startDate,
      endDate: event.endDate,
      dailySlots: event.dailySlots,
      subjectConfigs: event.subjectConfigs,
      rooms: event.rooms,
      examStatus: 'Planning',
      isAutoGenerated: false,
      createdAt: DateTime.now(),
    );
    await docRef.set(newEvent.toFirestore());
    return docRef.id;
  }

  /// Stream seluruh event UTS/UAS sekolah yang tidak diarsipkan (diurutkan terbaru)
  Stream<List<ExamEvent>> getExamEvents(String schoolId) {
    return _eventsRef(schoolId)
        .orderBy('createdAt', descending: true)
        .snapshots()
        .map((snap) {
          final events = snap.docs
              .map((d) => ExamEvent.fromFirestore(d))
              .where((e) => e.examStatus != 'Archived')
              .toList();
          events.sort((a, b) => b.createdAt.compareTo(a.createdAt));
          return events;
        });
  }

  /// Stream seluruh event UTS/UAS sekolah yang DIARSIPKAN (diurutkan terbaru)
  Stream<List<ExamEvent>> getArchivedExamEvents(String schoolId) {
    return _eventsRef(schoolId)
        .orderBy('createdAt', descending: true)
        .snapshots()
        .map((snap) {
          final events = snap.docs
              .map((d) => ExamEvent.fromFirestore(d))
              .where((e) => e.examStatus == 'Archived')
              .toList();
          events.sort((a, b) => b.createdAt.compareTo(a.createdAt));
          return events;
        });
  }

  /// Stream event yang sedang "Active" (untuk cek kondisi dashboard siswa)
  Stream<ExamEvent?> getActiveExamEvent(String schoolId) {
    return _eventsRef(schoolId)
        .where('examStatus', isEqualTo: 'Active')
        .limit(1)
        .snapshots()
        .map((snap) =>
            snap.docs.isEmpty ? null : ExamEvent.fromFirestore(snap.docs.first));
  }

  /// Stream event ujian terbaru yang active, untuk tampilan kartu siswa
  Stream<ExamEvent?> getLatestExamEvent(String schoolId) {
    return _eventsRef(schoolId)
        .snapshots()
        .map((snap) {
          final events = snap.docs
              .map((d) => ExamEvent.fromFirestore(d))
              .where((e) => e.examStatus == 'Active')
              .toList();
          if (events.isEmpty) return null;
          // Urutkan descending berdasarkan tanggal mulai, ambil yang terbaru
          events.sort((a, b) => b.startDate.compareTo(a.startDate));
          return events.first;
        });
  }

  /// Stream satu event berdasarkan ID
  Stream<ExamEvent?> getExamEventById(String schoolId, String eventId) {
    return _eventsRef(schoolId)
        .doc(eventId)
        .snapshots()
        .map((d) => d.exists ? ExamEvent.fromFirestore(d) : null);
  }

  /// Update status event (Planning → Active → Finished)
  /// Jika status menjadi Active, buat dokumen Exam di collection /exams untuk setiap sesi yang memiliki soal
  Future<void> updateExamEventStatus(
      String schoolId, String eventId, String status) async {
    if (status == 'Active') {
      // 1. Ambil detail event untuk tahu subjectConfigs
      final eventDoc = await _eventsRef(schoolId).doc(eventId).get();
      if (!eventDoc.exists) {
        throw Exception('Event tidak ditemukan.');
      }
      final eventData = eventDoc.data()!;
      final List subjectConfigsList = eventData['subjectConfigs'] as List? ?? [];

      // Build classId -> angkatan mapping from students list
      final studentsSnap = await _db
          .collection('schools')
          .doc(schoolId)
          .collection('students')
          .get();

      final Map<String, Map<String, int>> classAngkatanCounts = {};
      for (final doc in studentsSnap.docs) {
        final data = doc.data();
        final cid = data['classId'] as String? ?? '';
        final angkatan = (data['angkatan'] ?? '').toString().trim();
        final isLulus = data['lulus'] == true;
        final isAktif = data['aktif'] ?? true;
        if (cid.isNotEmpty && angkatan.isNotEmpty && !isLulus && isAktif) {
          classAngkatanCounts.putIfAbsent(cid, () => {});
          classAngkatanCounts[cid]![angkatan] = (classAngkatanCounts[cid]![angkatan] ?? 0) + 1;
        }
      }

      final Map<String, String> classIdToAngkatan = {};
      classAngkatanCounts.forEach((cid, counts) {
        if (counts.isNotEmpty) {
          classIdToAngkatan[cid] = counts.entries.reduce((a, b) => a.value > b.value ? a : b).key;
        }
      });

      // Ambil seluruh sesi untuk event ini
      final sessionsSnap = await _sessionsRef(schoolId)
          .where('eventId', isEqualTo: eventId)
          .get();

      // Group active angkatans by subjectId
      final Map<String, Set<String>> subjectActiveAngkatans = {};
      for (final doc in sessionsSnap.docs) {
        final sData = doc.data();
        final subId = sData['subjectId']?.toString() ?? '';
        final classId = sData['classId']?.toString() ?? '';
        
        final cids = classId.split(',').map((s) => s.trim()).where((s) => s.isNotEmpty);
        for (final cid in cids) {
          final angkatan = classIdToAngkatan[cid] ?? '';
          if (subId.isNotEmpty && angkatan.isNotEmpty) {
            subjectActiveAngkatans.putIfAbsent(subId, () => {}).add(angkatan);
          }
        }
      }

      // Validasi: Cek apakah ada mapel yang belum punya soal untuk angkatan yang berpartisipasi
      final List<String> subjectsWithoutQuestions = [];
      for (final item in subjectConfigsList) {
        final map = Map<String, dynamic>.from(item);
        final subId = map['subjectId']?.toString() ?? '';
        final subName = map['subjectName']?.toString() ?? 'Mata Pelajaran';
        if (subId.isNotEmpty) {
          final activeAngkatans = subjectActiveAngkatans[subId] ?? {};
          
          if (activeAngkatans.isEmpty) continue;
          
          for (final angkatan in activeAngkatans) {
            final docId = '${eventId}_${subId}_$angkatan';
            final qDoc = await _db
                .collection('schools')
                .doc(schoolId)
                .collection('exam_questions')
                .doc(docId)
                .get();
            
            bool hasQuestions = false;
            if (qDoc.exists && qDoc.data() != null) {
              final List questions = qDoc.data()!['questions'] as List? ?? [];
              if (questions.isNotEmpty) {
                hasQuestions = true;
              }
            }
            
            if (!hasQuestions) {
              subjectsWithoutQuestions.add('$subName (Angkatan $angkatan)');
            }
          }
        }
      }

      if (subjectsWithoutQuestions.isNotEmpty) {
        throw Exception(
          'Mata pelajaran berikut belum memiliki soal:\n'
          '${subjectsWithoutQuestions.map((s) => "- $s").join("\n")}',
        );
      }
    }

    // Update status di Firestore setelah validasi berhasil
    await _eventsRef(schoolId).doc(eventId).update({'examStatus': status});

    if (status == 'Finished') {
      try {
        final sessionsSnap = await _sessionsRef(schoolId).where('eventId', isEqualTo: eventId).get();
        for (final doc in sessionsSnap.docs) {
          await ExamBehaviorService().deleteExamBehaviorForSession(
            schoolId: schoolId,
            sessionId: doc.id,
          );
        }
      } catch (e) {
        // Abaikan error jika ada
      }
    }

    if (status == 'Active') {
      // Ambil detail event lagi untuk generate exams
      final eventDoc = await _eventsRef(schoolId).doc(eventId).get();
      if (!eventDoc.exists) return;
      final eventData = eventDoc.data()!;
      final examType = eventData['examType']?.toString() ?? 'UTS';
      final title = eventData['title']?.toString() ?? '';

      // Map subjectConfigs agar gampang diakses
      final List subjectConfigsList = eventData['subjectConfigs'] as List? ?? [];
      final Map<String, Map<String, dynamic>> subjectConfigsMap = {};
      for (final item in subjectConfigsList) {
        final map = Map<String, dynamic>.from(item);
        final subId = map['subjectId']?.toString() ?? '';
        if (subId.isNotEmpty) {
          subjectConfigsMap[subId] = map;
        }
      }

      // Ambil tahunAjaran dan semester dari school metadata
      final schoolDoc = await _db.collection('schools').doc(schoolId).get();
      final schoolData = schoolDoc.data() ?? {};
      final tahunAjaran = schoolData['tahunAjaran']?.toString() ?? '';
      final semester = schoolData['semester']?.toString() ?? '';

      // Build classId -> angkatan mapping from students list
      final studentsSnap = await _db
          .collection('schools')
          .doc(schoolId)
          .collection('students')
          .get();

      final Map<String, Map<String, int>> classAngkatanCounts = {};
      for (final doc in studentsSnap.docs) {
        final data = doc.data();
        final cid = data['classId'] as String? ?? '';
        final angkatan = (data['angkatan'] ?? '').toString().trim();
        final isLulus = data['lulus'] == true;
        final isAktif = data['aktif'] ?? true;
        if (cid.isNotEmpty && angkatan.isNotEmpty && !isLulus && isAktif) {
          classAngkatanCounts.putIfAbsent(cid, () => {});
          classAngkatanCounts[cid]![angkatan] = (classAngkatanCounts[cid]![angkatan] ?? 0) + 1;
        }
      }

      final Map<String, String> classIdToAngkatan = {};
      classAngkatanCounts.forEach((cid, counts) {
        if (counts.isNotEmpty) {
          classIdToAngkatan[cid] = counts.entries.reduce((a, b) => a.value > b.value ? a : b).key;
        }
      });

      // 2. Ambil semua sesi untuk event ini
      final sessionsSnap = await _sessionsRef(schoolId)
          .where('eventId', isEqualTo: eventId)
          .get();

      for (final sDoc in sessionsSnap.docs) {
        final sData = sDoc.data();
        final subjectId = sData['subjectId']?.toString() ?? '';
        final subjectName = sData['subjectName']?.toString() ?? '';
        final classId = sData['classId']?.toString() ?? '';
        final className = sData['className']?.toString() ?? '';

        // Dapatkan corrector/author ids & names dari config
        final config = subjectConfigsMap[subjectId];
        List<String> authorIds = [];
        List<String> authorNames = [];
        if (config != null) {
          if (config['authorTeacherIds'] != null) {
            authorIds = List<String>.from(config['authorTeacherIds']);
          } else if (config['authorTeacherId'] != null && config['authorTeacherId'].toString().isNotEmpty) {
            authorIds = [config['authorTeacherId'].toString()];
          }

          if (config['authorTeacherNames'] != null) {
            authorNames = List<String>.from(config['authorTeacherNames']);
          } else if (config['authorTeacherName'] != null && config['authorTeacherName'].toString().isNotEmpty) {
            authorNames = [config['authorTeacherName'].toString()];
          }
        }

        // Tentukan durasi
        int duration = 90;
        try {
          final start = sData['startTime']?.toString() ?? '07:30';
          final end = sData['endTime']?.toString() ?? '09:00';
          final startParts = start.split(':');
          final endParts = end.split(':');
          final startMin = int.parse(startParts[0]) * 60 + int.parse(startParts[1]);
          final endMin = int.parse(endParts[0]) * 60 + int.parse(endParts[1]);
          if (endMin > startMin) {
            duration = endMin - startMin;
          }
        } catch (_) {}

        // Ambil questions dari exam_questions/${eventId}_${subjectId}_${angkatan} atau fallback ke ${eventId}_${subjectId}
        final firstClassId = classId.split(',').first.trim();
        var angkatan = classIdToAngkatan[firstClassId] ?? '';
        if (angkatan.isEmpty) {
          final lvl = _getGradeLevel(className);
          if (lvl == '3') {
            angkatan = '2020';
          } else if (lvl == '2') {
            angkatan = '2021';
          } else if (lvl == '1') {
            angkatan = '2022';
          } else {
            angkatan = '2022';
          }
        }

        var qDoc = await _db
            .collection('schools')
            .doc(schoolId)
            .collection('exam_questions')
            .doc('${eventId}_${subjectId}_$angkatan')
            .get();

        if (!qDoc.exists || qDoc.data() == null) {
          qDoc = await _db
              .collection('schools')
              .doc(schoolId)
              .collection('exam_questions')
              .doc('${eventId}_$subjectId')
              .get();
        }

        if (qDoc.exists && qDoc.data() != null) {
          final qData = qDoc.data()!;
          final questionsList = qData['questions'] as List? ?? [];

          // Buat ID exam: eventId_subjectId_classId
          final examId = '${eventId}_${subjectId}_$classId';

          // Tulis ke collection /exams
          await _db
              .collection('schools')
              .doc(schoolId)
              .collection('exams')
              .doc(examId)
              .set({
            'title': '$examType $subjectName - $className',
            'description': 'Ujian Semester $examType - $title',
            'classId': classId,
            'className': className,
            'subjectId': subjectId,
            'subjectName': subjectName,
            'teacherId': authorIds.isNotEmpty ? authorIds.first : '',
            'teacherName': authorNames.isNotEmpty ? authorNames.join(', ') : 'Guru Penguji',
            'teacherIds': authorIds,
            'teacherNames': authorNames,
            'durationMinutes': duration,
            'syncToGrades': true,
            'gradeCategory': examType, // UTS atau UAS
            'tahunAjaran': tahunAjaran,
            'semester': semester,
            'createdAt': FieldValue.serverTimestamp(),
            'dueDate': Timestamp.fromDate((sData['date'] as Timestamp).toDate().add(const Duration(days: 1))),
            'status': 'active',
            'questions': questionsList,
            'susulanStudentIds': [],
          });
        }
      }
    }
  }

  /// Hapus event beserta seluruh sesinya (batch delete)
  Future<void> deleteExamEvent(String schoolId, String eventId) async {
    final List<DocumentReference> refsToDelete = [];

    // 1. Hapus semua sesi terkait (termasuk sub-koleksi participations)
    final sessions = await _sessionsRef(schoolId)
        .where('eventId', isEqualTo: eventId)
        .get();
    for (final sDoc in sessions.docs) {
      // Hapus participations dalam setiap sesi
      final parts = await sDoc.reference.collection('participations').get();
      for (final pDoc in parts.docs) {
        refsToDelete.add(pDoc.reference);
      }
      refsToDelete.add(sDoc.reference);

      // Bersihkan juga behavior records jika masih ada
      try {
        await ExamBehaviorService().deleteExamBehaviorForSession(
          schoolId: schoolId,
          sessionId: sDoc.id,
        );
      } catch (e) {
        // Abaikan
      }
    }

    // 2. Hapus exams yang digenerate oleh event ini
    final examsSnap = await _db
        .collection('schools')
        .doc(schoolId)
        .collection('exams')
        .where(FieldPath.documentId, isGreaterThanOrEqualTo: '${eventId}_')
        .where(FieldPath.documentId, isLessThanOrEqualTo: '${eventId}_\uf8ff')
        .get();
    for (final doc in examsSnap.docs) {
      refsToDelete.add(doc.reference);
    }

    // 3. Hapus exam_questions (bank soal) terkait event ini
    final questionsSnap = await _db
        .collection('schools')
        .doc(schoolId)
        .collection('exam_questions')
        .where(FieldPath.documentId, isGreaterThanOrEqualTo: '${eventId}_')
        .where(FieldPath.documentId, isLessThanOrEqualTo: '${eventId}_\uf8ff')
        .get();
    for (final doc in questionsSnap.docs) {
      refsToDelete.add(doc.reference);
    }

    // 4. Hapus grades terkait yang di-generate oleh event ini (termasuk kelas gabungan)
    final gradesSnap = await _db
        .collection('schools')
        .doc(schoolId)
        .collection('grades')
        .where(FieldPath.documentId, isGreaterThanOrEqualTo: '${eventId}_')
        .where(FieldPath.documentId, isLessThanOrEqualTo: '${eventId}_\uf8ff')
        .get();
    for (final doc in gradesSnap.docs) {
      refsToDelete.add(doc.reference);
    }

    // 5. Hapus exam submissions terkait event ini
    final submissionsSnap = await _db
        .collection('schools')
        .doc(schoolId)
        .collection('exam_submissions')
        .where(FieldPath.documentId, isGreaterThanOrEqualTo: '${eventId}_')
        .where(FieldPath.documentId, isLessThanOrEqualTo: '${eventId}_\uf8ff')
        .get();
    for (final doc in submissionsSnap.docs) {
      refsToDelete.add(doc.reference);
    }

    // 6. Hapus event document itu sendiri
    refsToDelete.add(_eventsRef(schoolId).doc(eventId));

    // Eksekusi penghapusan dalam chunk berukuran maksimal 400 dokumen per batch
    for (int i = 0; i < refsToDelete.length; i += 400) {
      final chunk = refsToDelete.sublist(
        i,
        i + 400 > refsToDelete.length ? refsToDelete.length : i + 400,
      );
      final chunkBatch = _db.batch();
      for (final ref in chunk) {
        chunkBatch.delete(ref);
      }
      await chunkBatch.commit();
    }
  }

