  /// Generate UUID v4 sederhana untuk qrToken
  static String generateQrToken() {
    final rng = Random.secure();
    final bytes = List<int>.generate(16, (_) => rng.nextInt(256));
    bytes[6] = (bytes[6] & 0x0f) | 0x40;
    bytes[8] = (bytes[8] & 0x3f) | 0x80;
    String hex(int n) => n.toRadixString(16).padLeft(2, '0');
    return '${hex(bytes[0])}${hex(bytes[1])}${hex(bytes[2])}${hex(bytes[3])}'
        '-${hex(bytes[4])}${hex(bytes[5])}'
        '-${hex(bytes[6])}${hex(bytes[7])}'
        '-${hex(bytes[8])}${hex(bytes[9])}'
        '-${hex(bytes[10])}${hex(bytes[11])}${hex(bytes[12])}${hex(bytes[13])}${hex(bytes[14])}${hex(bytes[15])}';
  }

  // ── Seat Distribution per existing session ────────────────────

  /// Distribusi kursi selang-seling untuk sesi yang sudah ada di Firestore.
  /// Menghapus data participations lama, lalu generate ulang dari daftar rooms yang diberikan.
  /// Algoritma: siswa 2 angkatan berbeda diinterleave (ganjil=angkatan A, genap=angkatan B).
  Future<void> distributeSeatsForExistingSession({
    required String schoolId,
    required String sessionId,
    required String classId,
    required List<ExamRoom> rooms,
  }) async {
    // 1. Fetch students in this class (handles comma-separated classIds)
    final List<String> targetClassIds = classId.split(',').map((e) => e.trim()).where((e) => e.isNotEmpty).toList();
    final List<Map<String, dynamic>> allStudents = [];

    for (final cid in targetClassIds) {
      final studentsSnap = await _db
          .collection('schools')
          .doc(schoolId)
          .collection('students')
          .where('classId', isEqualTo: cid)
          .get();

      allStudents.addAll(studentsSnap.docs.map((d) {
        final data = d.data();
        return {
          'id': d.id,
          'nama': data['nama'] ?? '',
          'nis': data['nis'] ?? '',
          'angkatan': (data['angkatan'] ?? '').toString().trim(),
        };
      }));
    }

    // 2. Group by angkatan
    final Map<String, List<Map<String, dynamic>>> byAngkatan = {};
    for (final s in allStudents) {
      final a = (s['angkatan'] as String).isNotEmpty ? s['angkatan'] as String : '-';
      byAngkatan.putIfAbsent(a, () => []).add(s);
    }

    // Sort: biggest cohort first for list A
    final sortedAngkatans = byAngkatan.keys.toList()
      ..sort((a, b) => byAngkatan[b]!.length.compareTo(byAngkatan[a]!.length));

    List<Map<String, dynamic>> listA = [];
    List<Map<String, dynamic>> listB = [];

    if (sortedAngkatans.isNotEmpty) {
      listA = List.from(byAngkatan[sortedAngkatans[0]]!)..shuffle();
    }
    if (sortedAngkatans.length > 1) {
      for (int i = 1; i < sortedAngkatans.length; i++) {
        listB.addAll(byAngkatan[sortedAngkatans[i]]!);
      }
      listB.shuffle();
    }

    // 3. Build participation list (interleaved by room)
    final List<ExamParticipation> participations = [];
    int indexA = 0;
    int indexB = 0;

    for (final room in rooms) {
      for (int seat = 1; seat <= room.capacity; seat++) {
        if (indexA >= listA.length && indexB >= listB.length) break;

        Map<String, dynamic>? picked;
        if (seat % 2 != 0) {
          picked = indexA < listA.length ? listA[indexA++] : (indexB < listB.length ? listB[indexB++] : null);
        } else {
          picked = indexB < listB.length ? listB[indexB++] : (indexA < listA.length ? listA[indexA++] : null);
        }

        if (picked != null) {
          participations.add(ExamParticipation(
            studentId: picked['id'] as String,
            studentName: picked['nama'] as String,
            nis: picked['nis'] as String,
            hasStarted: false,
            seatNumber: seat,
            roomName: room.name,
            angkatan: picked['angkatan'] as String,
          ));
        }
      }
    }

    // 4. Batch write: delete old participations, write new
    final pRef = _participationsRef(schoolId, sessionId);

    // Delete existing participations
    final existingDocs = await pRef.get();
    final batch = _db.batch();
    for (final doc in existingDocs.docs) {
      batch.delete(doc.reference);
    }

    // Write new participations
    for (final p in participations) {
      batch.set(pRef.doc(p.studentId), p.toFirestore());
    }

    // Update session roomName
    final roomNameStr = rooms.map((r) => r.name).join(', ');
    batch.update(_sessionsRef(schoolId).doc(sessionId), {'roomName': roomNameStr});

    await batch.commit();
  }

  /// Sinkronkan/deploy pertanyaan ujian ke koleksi /exams untuk seluruh sesi yang sesuai dengan eventId & subjectId
  Future<void> syncQuestionsToExams({
    required String schoolId,
    required String eventId,
    required String subjectId,
    required List<Map<String, dynamic>> questionsList,
    String? gradeLevel,
  }) async {
    // 1. Ambil detail event
    final eventDoc = await _eventsRef(schoolId).doc(eventId).get();
    if (!eventDoc.exists) return;
    final eventData = eventDoc.data()!;
    final examType = eventData['examType']?.toString() ?? 'UTS';
    final title = eventData['title']?.toString() ?? '';

    // Map subjectConfigs agar gampang diakses
    final List subjectConfigsList = eventData['subjectConfigs'] as List? ?? [];
    final Map<String, Map<String, dynamic>> subjectConfigsMap = {};
    for (final item in subjectConfigsList) {
      final map = Map<String, dynamic>.from(item);
      final subId = map['subjectId']?.toString() ?? '';
      if (subId.isNotEmpty) {
        subjectConfigsMap[subId] = map;
      }
    }

    // Ambil tahunAjaran dan semester dari school metadata
    final schoolDoc = await _db.collection('schools').doc(schoolId).get();
    final schoolData = schoolDoc.data() ?? {};
    final tahunAjaran = schoolData['tahunAjaran']?.toString() ?? '';
    final semester = schoolData['semester']?.toString() ?? '';

    // Build classId -> angkatan mapping from students list
    final studentsSnap = await _db
        .collection('schools')
        .doc(schoolId)
        .collection('students')
        .get();

    final Map<String, Map<String, int>> classAngkatanCounts = {};
    for (final doc in studentsSnap.docs) {
      final data = doc.data();
      final cid = data['classId'] as String? ?? '';
      final angkatan = (data['angkatan'] ?? '').toString().trim();
      final isLulus = data['lulus'] == true;
      final isAktif = data['aktif'] ?? true;
      if (cid.isNotEmpty && angkatan.isNotEmpty && !isLulus && isAktif) {
        classAngkatanCounts.putIfAbsent(cid, () => {});
        classAngkatanCounts[cid]![angkatan] = (classAngkatanCounts[cid]![angkatan] ?? 0) + 1;
      }
    }

    final Map<String, String> classIdToAngkatan = {};
    classAngkatanCounts.forEach((cid, counts) {
      if (counts.isNotEmpty) {
        classIdToAngkatan[cid] = counts.entries.reduce((a, b) => a.value > b.value ? a : b).key;
      }
    });

    // 2. Ambil seluruh sesi untuk event dan subject ini
    final sessionsSnap = await _sessionsRef(schoolId)
        .where('eventId', isEqualTo: eventId)
        .where('subjectId', isEqualTo: subjectId)
        .get();

    for (final sDoc in sessionsSnap.docs) {
      final sData = sDoc.data();
      final subjectName = sData['subjectName']?.toString() ?? '';
      final classId = sData['classId']?.toString() ?? '';
      final className = sData['className']?.toString() ?? '';

      final firstClassId = classId.split(',').first.trim();
      var sessionAngkatan = classIdToAngkatan[firstClassId] ?? '';
      if (sessionAngkatan.isEmpty) {
        final lvl = _getGradeLevel(className);
        if (lvl == '3') {
          sessionAngkatan = '2020';
        } else if (lvl == '2') {
          sessionAngkatan = '2021';
        } else if (lvl == '1') {
          sessionAngkatan = '2022';
        } else {
          sessionAngkatan = '2022';
        }
      }
      // Skip if gradeLevel filter is specified and doesn't match this class grade level (angkatan)
      if (gradeLevel != null && sessionAngkatan != gradeLevel) {
        continue;
      }

      // Dapatkan corrector/author ids & names dari config
      final config = subjectConfigsMap[subjectId];
      List<String> authorIds = [];
      List<String> authorNames = [];
      if (config != null) {
        if (config['authorTeacherIds'] != null) {
          authorIds = List<String>.from(config['authorTeacherIds']);
        } else if (config['authorTeacherId'] != null && config['authorTeacherId'].toString().isNotEmpty) {
          authorIds = [config['authorTeacherId'].toString()];
        }

        if (config['authorTeacherNames'] != null) {
          authorNames = List<String>.from(config['authorTeacherNames']);
        } else if (config['authorTeacherName'] != null && config['authorTeacherName'].toString().isNotEmpty) {
          authorNames = [config['authorTeacherName'].toString()];
        }
      }

      // Tentukan durasi
      int duration = 90;
      try {
        final start = sData['startTime']?.toString() ?? '07:30';
        final end = sData['endTime']?.toString() ?? '09:00';
        final startParts = start.split(':');
        final endParts = end.split(':');
        final startMin = int.parse(startParts[0]) * 60 + int.parse(startParts[1]);
        final endMin = int.parse(endParts[0]) * 60 + int.parse(endParts[1]);
        if (endMin > startMin) {
          duration = endMin - startMin;
        }
      } catch (_) {}

      // Buat ID exam: eventId_subjectId_classId
      final examId = '${eventId}_${subjectId}_$classId';

      // Tulis atau update ke collection /exams
      await _db
          .collection('schools')
          .doc(schoolId)
          .collection('exams')
          .doc(examId)
          .set({
        'title': '$examType $subjectName - $className',
        'description': 'Ujian Semester $examType - $title',
        'classId': classId,
        'className': className,
        'subjectId': subjectId,
        'subjectName': subjectName,
        'teacherId': authorIds.isNotEmpty ? authorIds.first : '',
        'teacherName': authorNames.isNotEmpty ? authorNames.join(', ') : 'Guru Penguji',
        'teacherIds': authorIds,
        'teacherNames': authorNames,
        'durationMinutes': duration,
        'syncToGrades': true,
        'gradeCategory': examType, // UTS atau UAS
        'tahunAjaran': tahunAjaran,
        'semester': semester,
        'createdAt': FieldValue.serverTimestamp(),
        'dueDate': Timestamp.fromDate((sData['date'] as Timestamp).toDate().add(const Duration(days: 1))),
        'status': 'active',
        'questions': questionsList,
        'susulanStudentIds': [],
      });
    }
  }
}

String _getGradeLevel(String className) {
  final cleanName = className.toUpperCase().trim();
  if (cleanName.startsWith('XII') || cleanName.contains('12')) return '3';
  if (cleanName.startsWith('XI') || cleanName.contains('11')) {
    if (!cleanName.startsWith('XII')) return '2';
  }
  if (cleanName.startsWith('X') || cleanName.contains('10')) {
    if (!cleanName.startsWith('XII') && !cleanName.startsWith('XI')) return '1';
  }
  if (cleanName.startsWith('IX') || cleanName.contains('9')) return '3';
  if (cleanName.startsWith('VIII') || cleanName.contains('8')) return '2';
  if (cleanName.startsWith('VII') || cleanName.contains('7')) {
    if (!cleanName.startsWith('VIII')) return '1';
  }
  if (cleanName.contains('6')) return '6';
  if (cleanName.contains('5')) return '5';
  if (cleanName.contains('4')) return '4';
  if (cleanName.contains('3')) return '3';
  if (cleanName.contains('2')) return '2';
  return '1';
}
