  // ── Utility ──────────────────────────────────────────────────

  /// Generate UUID v4 sederhana untuk qrToken
  static String generateQrToken() {
    final rng = Random.secure();
    final bytes = List<int>.generate(16, (_) => rng.nextInt(256));
    bytes[6] = (bytes[6] & 0x0f) | 0x40;
    bytes[8] = (bytes[8] & 0x3f) | 0x80;
    String hex(int n) => n.toRadixString(16).padLeft(2, '0');
    return '${hex(bytes[0])}${hex(bytes[1])}${hex(bytes[2])}${hex(bytes[3])}'
        '-${hex(bytes[4])}${hex(bytes[5])}'
        '-${hex(bytes[6])}${hex(bytes[7])}'
        '-${hex(bytes[8])}${hex(bytes[9])}'
        '-${hex(bytes[10])}${hex(bytes[11])}${hex(bytes[12])}${hex(bytes[13])}${hex(bytes[14])}${hex(bytes[15])}';
  }

  // ── Seat Distribution per existing session ────────────────────

  /// Distribusi kursi selang-seling untuk sesi yang sudah ada di Firestore.
  /// Menghapus data participations lama, lalu generate ulang dari daftar rooms yang diberikan.
  /// Algoritma: siswa 2 angkatan berbeda diinterleave (ganjil=angkatan A, genap=angkatan B).
  Future<void> distributeSeatsForExistingSession({
    required String schoolId,
    required String sessionId,
    required String classId,
    required List<ExamRoom> rooms,
  }) async {
    // 1. Fetch students in this class
    final studentsSnap = await _db
        .collection('schools')
        .doc(schoolId)
        .collection('students')
        .where('classId', isEqualTo: classId)
        .get();

    final allStudents = studentsSnap.docs.map((d) {
      final data = d.data();
      return {
        'id': d.id,
        'nama': data['nama'] ?? '',
        'nis': data['nis'] ?? '',
        'angkatan': (data['angkatan'] ?? '').toString().trim(),
      };
    }).toList();

    // 2. Group by angkatan
    final Map<String, List<Map<String, dynamic>>> byAngkatan = {};
    for (final s in allStudents) {
      final a = (s['angkatan'] as String).isNotEmpty ? s['angkatan'] as String : '-';
      byAngkatan.putIfAbsent(a, () => []).add(s);
    }

    // Sort: biggest cohort first for list A
    final sortedAngkatans = byAngkatan.keys.toList()
      ..sort((a, b) => byAngkatan[b]!.length.compareTo(byAngkatan[a]!.length));

    List<Map<String, dynamic>> listA = [];
    List<Map<String, dynamic>> listB = [];

    if (sortedAngkatans.isNotEmpty) {
      listA = List.from(byAngkatan[sortedAngkatans[0]]!)..shuffle();
    }
    if (sortedAngkatans.length > 1) {
      for (int i = 1; i < sortedAngkatans.length; i++) {
        listB.addAll(byAngkatan[sortedAngkatans[i]]!);
      }
      listB.shuffle();
    }

    // 3. Build participation list (interleaved by room)
    final List<ExamParticipation> participations = [];
    int indexA = 0;
    int indexB = 0;

    for (final room in rooms) {
      for (int seat = 1; seat <= room.capacity; seat++) {
        if (indexA >= listA.length && indexB >= listB.length) break;

        Map<String, dynamic>? picked;
        if (seat % 2 != 0) {
          picked = indexA < listA.length ? listA[indexA++] : (indexB < listB.length ? listB[indexB++] : null);
        } else {
          picked = indexB < listB.length ? listB[indexB++] : (indexA < listA.length ? listA[indexA++] : null);
        }

        if (picked != null) {
          participations.add(ExamParticipation(
            studentId: picked['id'] as String,
            studentName: picked['nama'] as String,
            nis: picked['nis'] as String,
            hasStarted: false,
            seatNumber: seat,
            roomName: room.name,
            angkatan: picked['angkatan'] as String,
          ));
        }
      }
    }

    // 4. Batch write: delete old participations, write new
    final pRef = _participationsRef(schoolId, sessionId);

    // Delete existing participations
    final existingDocs = await pRef.get();
    final batch = _db.batch();
    for (final doc in existingDocs.docs) {
      batch.delete(doc.reference);
    }

    // Write new participations
    for (final p in participations) {
      batch.set(pRef.doc(p.studentId), p.toFirestore());
    }

    // Update session roomName
    final roomNameStr = rooms.map((r) => r.name).join(', ');
    batch.update(_sessionsRef(schoolId).doc(sessionId), {'roomName': roomNameStr});

    await batch.commit();
  }
