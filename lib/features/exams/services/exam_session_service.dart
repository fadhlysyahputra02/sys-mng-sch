  /// Mencatat kehadiran siswa setelah scan QR (idempotent)
  Future<void> recordParticipation({
    required String schoolId,
    required String sessionId,
    required String studentId,
    required String studentName,
    required String nis,
  }) async {
    await _participationsRef(schoolId, sessionId).doc(studentId).set({
      'studentId': studentId,
      'studentName': studentName,
      'nis': nis,
      'scannedAt': FieldValue.serverTimestamp(),
      'hasStarted': false,
    }, SetOptions(merge: true));
  }

  /// Cek apakah siswa sudah scan (untuk gate tombol "Mulai Ujian")
  Future<bool> hasStudentScanned({
    required String schoolId,
    required String sessionId,
    required String studentId,
  }) async {
    final doc = await _participationsRef(schoolId, sessionId)
        .doc(studentId)
        .get();
    if (!doc.exists) return false;
    return doc.data()?['scannedAt'] != null;
  }

