  /// Menyimpan list ExamSession hasil generate ke Firestore beserta participations-nya
  Future<void> saveGeneratedSessions(
      String schoolId, List<ExamSession> sessions) async {
    for (final session in sessions) {
      final sessionRef = _sessionsRef(schoolId).doc();
      final batch = _db.batch();
      
      // Simpan session
      batch.set(sessionRef, session.toFirestore());
      
      // Simpan draf presensi & kursi jika ada
      if (session.previewParticipations != null) {
        for (final p in session.previewParticipations!) {
          final pRef = sessionRef.collection('participations').doc(p.studentId);
          batch.set(pRef, p.toFirestore());
        }
      }
      
      await batch.commit();
    }
  }

